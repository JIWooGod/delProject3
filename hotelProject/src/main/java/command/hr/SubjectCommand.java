package command.hr;

public class SubjectCommand {
	Long tchNo;
	String subjName;
	String subjVideo;
	String subjCnt;
	
	public Long getTchNo() {
		return tchNo;
	}
	public void setTchNo(Long tchNo) {
		this.tchNo = tchNo;
	}
	public String getSubjName() {
		return subjName;
	}
	public void setSubjName(String subjName) {
		this.subjName = subjName;
	}
	public String getSubjVideo() {
		return subjVideo;
	}
	public void setSubjVideo(String subjVideo) {
		this.subjVideo = subjVideo;
	}
	public String getSubjCnt() {
		return subjCnt;
	}
	public void setSubjCnt(String subjCnt) {
		this.subjCnt = subjCnt;
	}
}